import UIKit

extension UIColor {
  
  @nonobjc class var aWhite: UIColor {
    return UIColor(white: 1.0, alpha: 1.0)
  }
  
  @nonobjc class var aPink: UIColor {
    return UIColor(red: 236.0 / 255.0, green: 94.0 / 255.0, blue: 101.0 / 255.0, alpha: 1.0)
  }
  
  @nonobjc class var aTextPrimary: UIColor {
    return UIColor(white: 0.0, alpha: 1.0)
  }
  
  @nonobjc class var aTextSecondary: UIColor {
    return UIColor(white: 119.0 / 255.0, alpha: 1.0)
  }
  
}
